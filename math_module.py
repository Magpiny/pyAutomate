#  My custom math module
import math


# ADDITION

def add(a, b):
    summation = a + b
    print(summation)


# SUBTRACTION

def subtract(a, b):
    subtr = b - a
    print(subtr)


# MULTIPLICATION

def mult(a, b):
    times = a * b
    print(times)


# DIVISION


def div(a, b):
    divide = a / b
    print(divide)


# EXPONENTIAL


def exp(a, b):
    xpo = a ** b
    print(xpo)


# SQUARE

def sqr(a):
    sq = a ** 2
    print(sq)


# CUBE

def cube(a):
    cub = a ** 3
    print(cub)

# SQUAREROOT


def sqrt(a):
    root = math.sqrt(a)
    print(root)

# LOGARITHM to base 10


def log(a):
    lgrthm = math.log10(a)
    print(lgrthm)

